const THEME_BY_ID = new Map(TERMINAL_THEMES.map((theme) => [theme.id, theme]));

export function isTerminalThemeId(value) {
  return typeof value === 'string' && THEME_BY_ID.has(value);
}

export function getTerminalTheme(value) {
  return THEME_BY_ID.get(value) || THEME_BY_ID.get(DEFAULT_TERMINAL_THEME);
}
